def run_eda(input_csv: str, out_dir: str,
            comment_col=None, response_col=None,
            stage_col=None, class_col=None,
            category_col=None, subtype_col=None):
    outdir = Path(out_dir)
    safe_mkdir(outdir)

    df = read_csv_any(input_csv)
    df["__source_file"] = os.path.basename(input_csv)

    # Resolve columns (auto if None)
    comment_col = comment_col or guess_col(df, ["comment_bn","comment","comments","text","content","message","মন্তব্য","কমেন্ট"])
    response_col = response_col or guess_col(df, ["response_bn","response","reply","উত্তর","প্রতিক্রিয়া"])
    stage_col    = stage_col    or guess_col(df, ["stage_bn","stage","severity","স্টেজ","স্তর"])
    class_col    = class_col    or guess_col(df, ["class_bn","class","label","topic_key","টপিক","ক্লাস","শ্রেণী"])
    category_col = category_col or guess_col(df, ["category_bn","category","ক্যাটাগরি","ধরণ"])
    subtype_col  = subtype_col  or guess_col(df, ["subtype_bn","subtype","sub_class","সাবটাইপ"])

    resolved = {
        "comment_col": comment_col,
        "response_col": response_col,
        "stage_col": stage_col,
        "class_col": class_col,
        "category_col": category_col,
        "subtype_col": subtype_col
    }
    with open(outdir / "resolved_columns.json", "w", encoding="utf-8") as f:
        json.dump(resolved, f, ensure_ascii=False, indent=2)

    # Basic cleaning
    if comment_col and comment_col in df.columns:
        df = df[df[comment_col].astype(str).str.strip().str.len() > 0]
    if response_col and response_col in df.columns:
        df = df[df[response_col].astype(str).str.strip().str.len() > 0]

    df.to_csv(outdir / "cleaned.csv", index=False, encoding="utf-8-sig")

    # Text numeric features
    num_blocks = []
    if comment_col:  num_blocks.append(text_features(df[comment_col], "comment"))
    if response_col: num_blocks.append(text_features(df[response_col], "response"))
    num_df = pd.concat(num_blocks, axis=1) if num_blocks else pd.DataFrame()
    if not num_df.empty:
        num_df.to_csv(outdir / "text_numeric_features.csv", index=False, encoding="utf-8-sig")

    # Quick counts + bar plots
    def value_counts_plot(col, title, fname):
        if not col or col not in df.columns: return
        vc = df[col].astype(str).value_counts().head(20)
        vc.to_csv(outdir / f"value_counts_{fname}.csv", encoding="utf-8-sig")
        try:
            plt.figure()
            vc.plot(kind="bar")
            plt.title(title); plt.tight_layout()
            p = outdir / f"plot_{fname}.png"
            plt.savefig(p, dpi=160); plt.show(); plt.close()
        except Exception:
            pass

    value_counts_plot(stage_col, "Stage distribution", "stage")
    value_counts_plot(class_col, "Class (Top-20)", "class")
    value_counts_plot(category_col, "Category (Top-20)", "category")
    value_counts_plot(subtype_col, "Subtype (Top-20)", "subtype")

    # Crosstabs (row-normalized)
    def crosstab_norm(rcol, ccol, fname):
        if not rcol or not ccol: return
        if rcol not in df.columns or ccol not in df.columns: return
        tab = pd.crosstab(df[rcol].astype(str), df[ccol].astype(str), normalize="index")
        tab.to_csv(outdir / fname, encoding="utf-8-sig")

    crosstab_norm(stage_col, class_col, "stage_by_class.csv")
    crosstab_norm(stage_col, category_col, "stage_by_category.csv")
    crosstab_norm(stage_col, subtype_col, "stage_by_subtype.csv")

    # Build matrix-ready DF
    matrix_df = pd.DataFrame(index=df.index)
    if not num_df.empty:
        matrix_df = pd.concat([matrix_df, num_df], axis=1)
    cat_cols = [c for c in [stage_col, class_col, category_col, subtype_col, "__source_file"] if c and c in df.columns]
    for c in cat_cols:
        matrix_df[c] = df[c].astype(str)

    numeric_cols = [c for c in matrix_df.columns if matrix_df[c].dtype != 'O']
    categorical_cols = [c for c in matrix_df.columns if c not in numeric_cols]

    # Mixed correlation + heatmap
    if len(numeric_cols) + len(categorical_cols) > 0:
        M = mixed_correlation(matrix_df, numeric_cols, categorical_cols)
        M.to_csv(outdir / "correlation_matrix_mixed.csv", encoding="utf-8-sig")
        plot_heatmap(M, outdir / "correlation_matrix_mixed.png",
                     "Mixed Correlation (Pearson / Cramér’s V / η)")

    # PII/links quick scan
    pii = {}
    if comment_col:
        s = df[comment_col].astype(str)
        pii["comment_has_email"] = int(s.str.contains(EMAIL_RE).sum())
        pii["comment_has_phone"] = int(s.str.contains(PHONE_RE).sum())
        pii["comment_has_url"]   = int(s.str.contains(URL_RE).sum())
    if response_col:
        s = df[response_col].astype(str)
        pii["response_has_email"] = int(s.str.contains(EMAIL_RE).sum())
        pii["response_has_phone"] = int(s.str.contains(PHONE_RE).sum())
        pii["response_has_url"]   = int(s.str.contains(URL_RE).sum())

    # Stage sanity
    stage_unexpected = 0
    if stage_col and stage_col in df.columns:
        st_bad = ~df[stage_col].astype(str).str.strip().str.lower().isin({s.lower() for s in STAGE_SET})
        stage_unexpected = int(st_bad.sum())

    summary = {
        "input_csv": input_csv,
        "rows": int(len(df)),
        "columns": list(df.columns),
        "resolved_columns": resolved,
        "numeric_features": numeric_cols if len(numeric_cols) else [],
        "categorical_features": categorical_cols if len(categorical_cols) else [],
        "stage_unexpected_count": stage_unexpected,
        "pii_scan": pii
    }
    with open(outdir / "summary.json", "w", encoding="utf-8") as f:
        json.dump(summary, f, ensure_ascii=False, indent=2)

    print("[OK] EDA complete.")
    print("Outputs saved in:", str(outdir.resolve()))

