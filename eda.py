def read_csv_any(path: str) -> pd.DataFrame:
    try:
        return pd.read_csv(path)
    except UnicodeDecodeError:
        return pd.read_csv(path, encoding="utf-8-sig")

def is_bangla_ratio(text: str) -> float:
    if not isinstance(text, str) or not text.strip(): return 0.0
    return len(BN_RANGE.findall(text)) / max(len(text), 1)

def text_features(s: pd.Series, prefix: str) -> pd.DataFrame:
    x = s.fillna("").astype(str)
    df = pd.DataFrame({
        f"{prefix}_len": x.str.len(),
        f"{prefix}_words": x.str.split().map(len),
        f"{prefix}_bangla_ratio": x.map(is_bangla_ratio),
        f"{prefix}_has_email": x.str.contains(EMAIL_RE).astype(int),
        f"{prefix}_has_phone": x.str.contains(PHONE_RE).astype(int),
        f"{prefix}_has_url": x.str.contains(URL_RE).astype(int),
        f"{prefix}_exclam": x.str.count("!"),
        f"{prefix}_qmark": x.str.count(r"\?"),
        f"{prefix}_comma": x.str.count(","),
        f"{prefix}_dot": x.str.count("।") + x.str.count(r"\."),
    })
    return df

def guess_col(df: pd.DataFrame, candidates):
    lower = {c.lower(): c for c in df.columns}
    for c in candidates:
        if c.lower() in lower:
            return lower[c.lower()]

    if any(k in candidates[0].lower() for k in ["comment","response","মন্তব্য","কমেন্ট","উত্তর","প্রতিক্রিয়া"]):
        obj_cols = [c for c in df.columns if df[c].dtype == "object"]
        if obj_cols:
            lens = {c: df[c].dropna().astype(str).str.len().mean() for c in obj_cols}
            return max(lens, key=lens.get) if lens else None
    return None


def pearson_num(x: pd.Series, y: pd.Series) -> float:
    a = pd.to_numeric(x, errors="coerce"); b = pd.to_numeric(y, errors="coerce")
    mask = ~(a.isna() | b.isna())
    if mask.sum() < 3: return np.nan
    return float(np.corrcoef(a[mask], b[mask])[0,1])

def cramers_v_corrected(x: pd.Series, y: pd.Series) -> float:
    tbl = pd.crosstab(x, y)
    if tbl.size == 0: return np.nan
    chi2, _, _, _ = chi2_contingency(tbl, correction=False)
    n = tbl.values.sum()
    if n == 0: return np.nan
    phi2 = chi2 / n
    r, k = tbl.shape
    phi2corr = max(0, phi2 - ((k-1)*(r-1))/(n-1))
    rcorr = r - ((r-1)**2)/(n-1)
    kcorr = k - ((k-1)**2)/(n-1)
    denom = min((kcorr-1), (rcorr-1))
    if denom <= 0: return np.nan
    return float(np.sqrt(phi2corr / denom))

def correlation_ratio(categories: pd.Series, measurements: pd.Series) -> float:
    try:
        cats = categories.astype("category").cat.codes.values
        y = pd.to_numeric(measurements, errors="coerce").values
        mask = ~np.isnan(y)
        cats, y = cats[mask], y[mask]
        if len(y) == 0: return np.nan
        y_mean = y.mean()
        classes = np.unique(cats)
        ss_between = 0.0
        for c in classes:
            y_c = y[cats == c]
            if len(y_c) == 0: continue
            ss_between += len(y_c) * (y_c.mean() - y_mean) ** 2
        ss_total = ((y - y_mean) ** 2).sum()
        return float(np.sqrt(ss_between / ss_total)) if ss_total > 0 else np.nan
    except Exception:
        return np.nan

def mixed_correlation(df: pd.DataFrame, num_cols, cat_cols) -> pd.DataFrame:
    cols = num_cols + cat_cols
    M = pd.DataFrame(np.nan, index=cols, columns=cols)
    # numeric-numeric
    for i, c1 in enumerate(num_cols):
        M.loc[c1, c1] = 1.0
        for c2 in num_cols[i+1:]:
            r = pearson_num(df[c1], df[c2])
            M.loc[c1, c2] = M.loc[c2, c1] = r
    # categorical-categorical
    for i, c1 in enumerate(cat_cols):
        M.loc[c1, c1] = 1.0
        for c2 in cat_cols[i+1:]:
            v = cramers_v_corrected(df[c1].astype(str), df[c2].astype(str))
            M.loc[c1, c2] = M.loc[c2, c1] = v
    # categorical-numeric (η)
    for c in cat_cols:
        for n in num_cols:
            eta = correlation_ratio(df[c].astype(str), df[n])
            M.loc[c, n] = M.loc[n, c] = eta
    return M

def plot_heatmap(matrix: pd.DataFrame, out_png: Path, title: str):
    fig, ax = plt.subplots(figsize=(max(8, len(matrix.columns)*0.6), max(6, len(matrix.index)*0.6)))
    im = ax.imshow(matrix.values, vmin=-1, vmax=1, aspect='auto')
    ax.set_xticks(range(len(matrix.columns)))
    ax.set_yticks(range(len(matrix.index)))
    ax.set_xticklabels(matrix.columns, rotation=90)
    ax.set_yticklabels(matrix.index)
    ax.set_title(title)
    cbar = plt.colorbar(im)
    cbar.set_label("corr / Cramér’s V / η")
    plt.tight_layout()
    plt.savefig(out_png, dpi=180)
    plt.show()
    plt.close()

